        txtDiaChi.setText(model.getValueAt(i, 5).toString());
        txtTrinhDo.setText(model.getValueAt(i, 6).toString());
        txtHocHam.setText(model.getValueAt(i, 7).toString());
        txtViTriThuViec.setText(model.getValueAt(i, 8).toString());
        txtGhiChu.setText(model.getValueAt(i, 10).toString());
