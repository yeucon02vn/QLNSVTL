    public ArrayList<QLHoSo> hsList() {
        ArrayList<QLHoSo> hsList = new ArrayList<>();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            conn = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=QLNS;" + "username=sa;password=123456");
            PreparedStatement ps = conn.prepareStatement("SELECT * FROM TblHoSoThuViec");
            ResultSet rs = ps.executeQuery();
            QLHoSo hs;
            while (rs.next()) {
                hs = new QLHoSo(
                    rs.getString("MaPhong"),
                    rs.getString("MaNV"),
                    rs.getString("HoTen"),
                    rs.getString("NgaySinh"),
                    rs.getString("GioiTinh"),
                    rs.getString("DiaChi"),
                    rs.getString("TDHocVan"),
                    rs.getString("HocHam"),
                    rs.getString("ViTriThuViec"),
                    rs.getString("NgayTV"),
                    rs.getString("GhiChu")
                );
                hsList.add(hs);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return hsList;
    }
    
